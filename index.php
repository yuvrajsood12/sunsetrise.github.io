    <h2 style="text-align: center;font-weight:bold;color:white;background-color:black;" id="about">HISTORY</h2>
    <br>
    <div class="container">
        <div class="row">
            <div class="col-sm-6">
                <p>The first verifiable claim for a practically used bicycle belongs to <span style="font-weight: bold;">German Baron Karl von Drais</span>, a civil servant to the Grand Duke of Baden in Germany. Drais invented his Laufmaschine (German for
                    "running machine") in 1817, that was called Draisine (English) or draisienne (French) by the press. Karl von Drais patented this design in 1818, which was the first commercially successful two-wheeled, steerable, human-propelled machine,
                    commonly called a velocipede, and nicknamed hobby-horse or dandy horse. It was initially manufactured in Germany and France.</p>
                <p>Hans-Erhard Lessing (Drais's biographer) found from circumstantial evidence that Drais's interest in finding an alternative to the horse was the starvation and death of horses caused by crop failure in 1816, the Year Without a Summer (following
                    the volcanic eruption of Tambora in 1815).</p>
                <p>On his first reported ride from Mannheim on June 12, 1817, he covered 13 km (eight miles) in less than an hour.[11] Constructed almost entirely of wood, the draisine weighed 22 kg (48 pounds), had brass bushings within the wheel bearings,
                    iron shod wheels, a rear-wheel brake and 152 mm (6 inches) of trail of the front-wheel for a self-centering caster effect. This design was welcomed by mechanically minded men daring to balance, and several thousand copies were built
                    and used, primarily in Western Europe and in North America. Its popularity rapidly faded when, partly due to increasing numbers of accidents, some city authorities began to prohibit its use. However, in 1866 Paris a Chinese visitor
                    named Bin Chun could still observe foot-pushed velocipedes.</p>
