                <p style="margin-top: 80px;">This bike is designed with excellent braking systems and shock-absorbing features that can easily handle serious bumps, rocks, dirt trails, roots and ruts. Mountain bikes are meant for dealing with steeper terrain which is why most of
                    them consist of lower gears as compared to most other road bikes.</p>
                <p>They usually have 26-inch or 29-inch wide knobby tires that make them function amazingly well over obstacles and in loose dirt. They also consist of rugged components and frames, flat handlebars and suspension in order to assist cyclists
