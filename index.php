                <h2 style="text-align: center;background-color:black;color:white;">ROAD BIKES</h2>
                <p style="margin-top: 100px;"><span style="font-weight: bold;">Road bikes</span> are best identified by their drop or turned-down handlebars and skinny tires. The downward-curving handlebars are usually super lightweight that help put you in an aerodynamic position.
                    The lightweight frame makes this type of bicycle particularly good for numerous pavement uses including racing, touring, fitness riding, long-distance rides and daily commuting.</p>
                <p>The large, thin tires allow you to glide the bike along various terrains with zero to little effort. If you are supremely concerned about speed, varied options for hand and riding positions, and an efficient transference of energy into
